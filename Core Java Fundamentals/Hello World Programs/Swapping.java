
public class Swapping {
	public static void main(String[] args) {
		int a =10;
		int b= 20;
		 int c = a;
		 a=b;
		 b=c;
		 
		 System.out.println(a);
		 System.out.println(b);
		 
		 int x = 9;
		 int y = 10;
		 
		 x = x+y;
		 y = x-y;
		 x = x-y;
		 
		 System.out.println(x);
		 System.out.println(y);
		 
	}
}
